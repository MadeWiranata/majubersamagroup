    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        // TODO add your handling code here:
      login a=new login();
        a.setVisible(true);
        dispose();
        if(Login.getText().trim().equals("Logout")){
                    bdataanggota.setEnabled(false);
                    bdatabuku.setEnabled(false);
                    bdatapeminjaman.setEnabled(false);
                    bdatapetugas.setEnabled(false);
                    bdatapengembalian.setEnabled(false);
                    Login.setText("Login");
                    PanelLogin.setVisible(false);
        }else if(Login.getText().trim().equals("Login")){
            PanelLogin.setVisible(true);
            jPasswordUser.requestFocus();
        }
    }//GEN-LAST:event_LoginActionPerformed

