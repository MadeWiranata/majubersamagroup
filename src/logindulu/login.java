    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        
        try {
            Connection koneksi=(Connection) DriverManager.getConnection("jdbc:mysql://localhost/db_perpustakaan","root","123");
            Statement stat= (Statement) koneksi.createStatement();
            ResultSet rs=stat.executeQuery("select * from tbl_petugas where nm_petugas='"+jPasswordUser.getText()+"' "
                    + "and password='"+jPasswordPass.getText()+"' and akses='"+cmbLevel.getSelectedItem()+"'" );
            rs.last();
            if(rs.getRow()>=1){
                    if(cmbLevel.getSelectedItem().equals("Admin")){
                    bdataanggota.setEnabled(true);
                    bdatabuku.setEnabled(true);
                    bdatapeminjaman.setEnabled(true);
                    bdatapetugas.setEnabled(true);
                    bdatapengembalian.setEnabled(true);
                    Login.setText("Logout");
                    PanelLogin.setVisible(false);
                    jPasswordPass.setText("");
                    jPasswordUser.setText("");
                    cmbLevel.setSelectedItem("Admin");
                    }else if(cmbLevel.getSelectedItem().equals("Petugas")){
                        bdataanggota.setEnabled(true);
                        bdatabuku.setEnabled(true);
                        bdatapeminjaman.setEnabled(true);
                        bdatapengembalian.setEnabled(true);
                        Login.setText("Logout");
                        PanelLogin.setVisible(false);
                        jPasswordPass.setText("");
                        jPasswordUser.setText("");
                        cmbLevel.setSelectedItem("Admin");
                    }else if(cmbLevel.getSelectedItem().equals("Administrasi")){
                        bdatapeminjaman.setEnabled(true);
                        bdatapengembalian.setEnabled(true);   
                        Login.setText("Logout");
                        PanelLogin.setVisible(false);
                        jPasswordPass.setText("");
                        jPasswordUser.setText("");
                        cmbLevel.setSelectedItem("Admin");
                    }else if((rs.getRow()==0)){
                    JOptionPane.showMessageDialog(null,"Maaf, "
                            + "Gagal masuk ID Pengguna atau Kata Sandi "
                            + "atau Level Pengguna tidak sesuai ...!");
                    bdataanggota.setEnabled(false);
                    bdatabuku.setEnabled(false);
                    bdatapeminjaman.setEnabled(false);
                    bdatapetugas.setEnabled(false);
                    bdatapengembalian.setEnabled(false);
                    Login.setText("Login");
                }
                   stat.close(); 
                    } 
        
        } catch (SQLException e) {
            System.out.println("Error" +e);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

